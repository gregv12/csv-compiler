package com.fluxtion.extension.csvcompiler.annotations;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * Marks a method in the bean record for post-processing, called after all fields from the input file have been set on
 * the bean and before being published to downstream consumers. Gives the developer the chance to change the internal
 * state of the bean before external consumption. The method annotated must be public scope and accept zero arguments.
 */
@Target(ElementType.METHOD)
@Retention(RetentionPolicy.SOURCE)
public @interface PostProcessMethod {
}
